#!/bin/bash
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –∑–∞–ø—É—Å–∫–∞ —Å–∏—Å—Ç–µ–º—ã –Ω–∞ –ø—Ä–æ–¥–∞–∫—à–Ω —Å–µ—Ä–≤–µ—Ä–µ
# –í—ã–ø–æ–ª–Ω–∏—Ç–µ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ: bash scripts/start_production.sh

set -e

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

echo -e "${GREEN}üöÄ –ó–ê–ü–£–°–ö –°–ò–°–¢–ï–ú–´ –ù–ê –ü–†–û–î–ê–ö–®–ù${NC}"
echo "=================================="

# –ü–µ—Ä–µ—Ö–æ–¥ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ø—Ä–æ–µ–∫—Ç–∞
cd /root/atra || {
    echo -e "${RED}‚ùå –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è /root/atra –Ω–µ –Ω–∞–π–¥–µ–Ω–∞!${NC}"
    exit 1
}

echo -e "\n${YELLOW}1. –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ç–µ–∫—É—â–µ–≥–æ –ø—Ä–æ—Ü–µ—Å—Å–∞...${NC}"
PID=$(ps aux | grep "python.*main.py" | grep -v grep | awk '{print $2}')
if [ -n "$PID" ]; then
    echo -e "${YELLOW}‚ö†Ô∏è  –ù–∞–π–¥–µ–Ω –∑–∞–ø—É—â–µ–Ω–Ω—ã–π –ø—Ä–æ—Ü–µ—Å—Å (PID: $PID)${NC}"
    read -p "–û—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –∏ –ø–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç—å? (y/n): " -n 1 -r
    echo
    if [[ $REPLY =~ ^[Yy]$ ]]; then
        echo "–û—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–æ—Ü–µ—Å—Å–∞..."
        kill -SIGTERM "$PID" || true
        sleep 5
        if ps -p "$PID" > /dev/null 2>&1; then
            echo "–ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏–µ..."
            kill -9 "$PID" || true
        fi
        echo -e "${GREEN}‚úÖ –ü—Ä–æ—Ü–µ—Å—Å –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω${NC}"
    else
        echo -e "${YELLOW}‚ÑπÔ∏è  –û—Å—Ç–∞–≤–ª—è–µ–º —Ç–µ–∫—É—â–∏–π –ø—Ä–æ—Ü–µ—Å—Å${NC}"
        exit 0
    fi
else
    echo -e "${GREEN}‚úÖ –ü—Ä–æ—Ü–µ—Å—Å –Ω–µ –Ω–∞–π–¥–µ–Ω (–º–æ–∂–Ω–æ –∑–∞–ø—É—Å–∫–∞—Ç—å)${NC}"
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏
echo -e "\n${YELLOW}2. –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏...${NC}"
if [ -f env ]; then
    ATRA_ENV=$(grep "^ATRA_ENV=" env | cut -d'=' -f2)
    echo "ATRA_ENV: $ATRA_ENV"
    if [ "$ATRA_ENV" != "prod" ]; then
        echo -e "${YELLOW}‚ö†Ô∏è  ATRA_ENV –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –≤ prod, –æ–±–Ω–æ–≤–ª—è—é...${NC}"
        sed -i 's/^ATRA_ENV=.*/ATRA_ENV=prod/' env
        echo -e "${GREEN}‚úÖ –û–±–Ω–æ–≤–ª–µ–Ω–æ${NC}"
    fi
else
    echo -e "${RED}‚ö†Ô∏è  –§–∞–π–ª env –Ω–µ –Ω–∞–π–¥–µ–Ω!${NC}"
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–ø–∏—Å–∫–∞ –º–æ–Ω–µ—Ç
echo -e "\n${YELLOW}3. –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–ø–∏—Å–∫–∞ –º–æ–Ω–µ—Ç...${NC}"
python3 -c "from config import COINS; print('COINS:', COINS)" 2>&1 | head -3

# –ê–∫—Ç–∏–≤–∞—Ü–∏—è –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è
echo -e "\n${YELLOW}4. –ê–∫—Ç–∏–≤–∞—Ü–∏—è –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è...${NC}"
if [ -d ".venv" ]; then
    source .venv/bin/activate
    echo -e "${GREEN}‚úÖ –í–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω–æ${NC}"
elif [ -d "venv" ]; then
    source venv/bin/activate
    echo -e "${GREEN}‚úÖ –í–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω–æ${NC}"
else
    echo -e "${YELLOW}‚ÑπÔ∏è  –í–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ, –∏—Å–ø–æ–ª—å–∑—É–µ–º —Å–∏—Å—Ç–µ–º–Ω—ã–π Python${NC}"
fi

# –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–ª—è –ª–æ–≥–æ–≤
echo -e "\n${YELLOW}5. –ü–æ–¥–≥–æ—Ç–æ–≤–∫–∞ –ª–æ–≥–æ–≤...${NC}"
mkdir -p logs
echo -e "${GREEN}‚úÖ –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è logs –≥–æ—Ç–æ–≤–∞${NC}"

# –ó–∞–ø—É—Å–∫ —Å–∏—Å—Ç–µ–º—ã
echo -e "\n${YELLOW}6. –ó–∞–ø—É—Å–∫ —Å–∏—Å—Ç–µ–º—ã...${NC}"
nohup python3 main.py > logs/atra.log 2>&1 &
NEW_PID=$!

# –û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–ø—É—Å–∫–∞ –∏ —Å–æ–∑–¥–∞–Ω–∏—è –ª–æ–≥-—Ñ–∞–π–ª–∞
echo "–û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–ø—É—Å–∫–∞ —Å–∏—Å—Ç–µ–º—ã..."
for i in {1..10}; do
    if ps -p $NEW_PID > /dev/null 2>&1; then
        if [ -f logs/atra.log ] && [ -s logs/atra.log ]; then
            break
        fi
    fi
    sleep 1
done

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–∞–ø—É—Å–∫–∞
if ps -p $NEW_PID > /dev/null 2>&1; then
    echo -e "${GREEN}‚úÖ –°–∏—Å—Ç–µ–º–∞ –∑–∞–ø—É—â–µ–Ω–∞ —É—Å–ø–µ—à–Ω–æ!${NC}"
    echo -e "${GREEN}   PID: $NEW_PID${NC}"
    echo -e "${GREEN}   –õ–æ–≥–∏: logs/atra.log${NC}"
    echo ""
    if [ -f logs/atra.log ] && [ -s logs/atra.log ]; then
        echo -e "${YELLOW}–ü—Ä–æ–≤–µ—Ä–∫–∞ –ª–æ–≥–æ–≤ (–ø–æ—Å–ª–µ–¥–Ω–∏–µ 20 —Å—Ç—Ä–æ–∫):${NC}"
        tail -20 logs/atra.log
    else
        echo -e "${YELLOW}‚ö†Ô∏è  –õ–æ–≥-—Ñ–∞–π–ª –µ—â–µ —Å–æ–∑–¥–∞–µ—Ç—Å—è, –ø–æ–¥–æ–∂–¥–∏—Ç–µ –Ω–µ—Å–∫–æ–ª—å–∫–æ —Å–µ–∫—É–Ω–¥${NC}"
    fi
    echo ""
    echo -e "${GREEN}–î–ª—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ:${NC}"
    echo "  tail -f logs/atra.log"
else
    echo -e "${RED}‚ùå –û—à–∏–±–∫–∞ –∑–∞–ø—É—Å–∫–∞!${NC}"
    if [ -f logs/atra.log ]; then
        echo -e "${RED}–ü–æ—Å–ª–µ–¥–Ω–∏–µ —Å—Ç—Ä–æ–∫–∏ –ª–æ–≥–∞:${NC}"
        tail -50 logs/atra.log
    else
        echo -e "${RED}–õ–æ–≥-—Ñ–∞–π–ª –Ω–µ —Å–æ–∑–¥–∞–Ω, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –æ—à–∏–±–∫–∏ –∑–∞–ø—É—Å–∫–∞${NC}"
    fi
    exit 1
fi

